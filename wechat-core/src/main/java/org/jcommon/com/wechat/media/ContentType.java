	public static ContentType getContentByType(String type_name, boolean md5){
		for(ContentType type : ContentType.class.getEnumConstants()){
			String type_str = type.type;
			try {
				type_str = md5?MD5.getMD5(type_str.getBytes()):type_str;
			} catch (NoSuchAlgorithmException e) {
				// TODO Auto-generated catch block
				logger.error("", e);
			}
			if(type_str.equalsIgnoreCase(type_name))
				return type;
		}
		return html;
	}
	
	public static ContentType getContentByType(String type_name){
		return getContentByType(type_name,false);
	}
	
	public static ContentType getContentType(String name){
		for(ContentType type : ContentType.class.getEnumConstants()){
			if(type.name.equalsIgnoreCase(name))
				return type;
		}
		return html;
	}
	
	public static ContentType getContentType(File file){
		if(file!=null && file.exists()){
			String file_name  = file.getName();
			String file_type  = file_name.indexOf(".")!=-1?file_name.substring(file_name.lastIndexOf(".")):"";
			return getContentType(file_type);
		}
		return html;
	}
